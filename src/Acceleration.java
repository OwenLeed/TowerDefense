
public class Acceleration {

	private double xAcc;
	private double yAcc;
	
	public Acceleration(double x, double y) {
		this.xAcc = x;
		this.yAcc = y;
	}

	public double getxAcc() {
		return xAcc;
	}

	public void setxAcc(double xAcc) {
		this.xAcc = xAcc;
	}

	public double getyAcc() {
		return yAcc;
	}

	public void setyAcc(double yAcc) {
		this.yAcc = yAcc;
	}
}
